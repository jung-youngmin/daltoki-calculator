const getIdx = (chapter: number, stage: number) => {
	switch (chapter) {
		case 5:
			return stage - 1;
		case 6:
			return CHAP_5_STAGES + stage - 1;
		case 7:
			return CHAP_5_STAGES + CHAP_6_STAGES + stage - 1;
		case 8:
			return CHAP_5_STAGES + CHAP_6_STAGES + CHAP_7_STAGES + stage - 1;
		case 9:
			return CHAP_5_STAGES + CHAP_6_STAGES + CHAP_7_STAGES + CHAP_8_STAGES + stage - 1;
		default:
			return 0;
	}
};

const makeStageData = (chapter: number, maxStageCnt: number): IHuntingGround[] => {
	const stageData: IHuntingGround[] = [];
	for (let i = 1; i <= maxStageCnt; i++) {
		const dataIdx = getIdx(chapter, i);
		const nextClimbing =
			i === maxStageCnt
				? climbingEfficiency[getIdx(chapter + 1, 1)]
				: climbingEfficiency[dataIdx + 1];

		stageData.push({
			name: `${chapter}-${i}`,
			chapter: chapter,
			stage: i,
			climbingEfficiency: climbingEfficiency[dataIdx],
			nextClimbingEfficiency: nextClimbing,
			weapon: weaponData[dataIdx],
		});
	}
	return stageData;
};

